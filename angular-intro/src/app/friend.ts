export class Friend {
    fName:string;
    lName:string;
    email:string;
    phoneNumber:string;
    language:string
    

    constructor(fname:string, lname:string, email:string, phoneNumber:string, language:string) {
         this.fName = fname;
         this.lName = lname;
         this.email = email;
         this.phoneNumber = phoneNumber;
         this.language = language
    }
}
